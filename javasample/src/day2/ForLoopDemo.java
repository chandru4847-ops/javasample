package day2;

public class ForLoopDemo {
public static void main(String[] args) {
		
		System.out.println("Numbers from 1 to 5:");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println("\nEven numbers from 2 to 10:");
        for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }

	}

}
