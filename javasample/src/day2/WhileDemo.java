package day2;

public class WhileDemo {
	public static void main(String[] args) {
		
		int i = 1;
	
	    System.out.println("Numbers from 1 to 5:");
	    while (i <= 5) {
	        System.out.println(i);
	        i++;
	    }
	
	    
	    int num = 1;
	    int sum = 0;
	
	    while (num <= 10) {
	        sum += num;
	        num++;
	    }
	    System.out.println("\nSum of numbers from 1 to 10 = " + sum);

	 }

}
